package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int C = 99;
        int H = 104;
        int A = 97;
        int R = 114;

        char c=(char)C;
        char h=(char)H;
        char a=(char)A;
        char r=(char)R;

        System.out.print(c);
        System.out.print(h);
        System.out.print(a);
        System.out.print(r);
    }
}
